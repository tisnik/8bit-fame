int eq(double a, double b) {
    return a == b;
}

int ne(double a, double b) {
    return a != b;
}

int lt(double a, double b) {
    return a < b;
}

int le(double a, double b) {
    return a <= b;
}

int gt(double a, double b) {
    return a > b;
}

int ge(double a, double b) {
    return a >= b;
}

