int eq(long long a, long long b) {
    return a == b;
}

int ne(long long a, long long b) {
    return a != b;
}

int lt(long long a, long long b) {
    return a < b;
}

int le(long long a, long long b) {
    return a <= b;
}

int gt(long long a, long long b) {
    return a > b;
}

int ge(long long a, long long b) {
    return a >= b;
}

