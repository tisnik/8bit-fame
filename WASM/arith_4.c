double fadd(double a, double b) {
    return a + b;
}

double fsub(double a, double b) {
    return a - b;
}

double fmul(double a, double b) {
    return a * b;
}

double fdiv(double a, double b) {
    return a / b;
}

double sqrt(double x);
double fabs(double x);

double fsqrt(double a) {
    return sqrt(a);
}

double fabs_(double a) {
    return fabs(a);
}
