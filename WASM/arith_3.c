float fadd(float a, float b) {
    return a + b;
}

float fsub(float a, float b) {
    return a - b;
}

float fmul(float a, float b) {
    return a * b;
}

float fdiv(float a, float b) {
    return a / b;
}

float sqrtf(float x);
float fabsf(float x);

float fsqrt(float a) {
    return sqrtf(a);
}

float fabs(float a) {
    return fabsf(a);
}
