void fill_in_ppu_ram(void)
{
    vram_adr(NTADR_A(0, 0));
    for (i = 0; i < 32 * 24; i++) {
        vram_put(nametable[i]);
    }
    for (i = 0; i < 32 * 6; i++) {
        vram_put(0x24);
    }
    for (i = 0; i < 32 * 24; i++) {
        vram_put(i);
    }
}

void fill_in_attributes(void)
{
    vram_adr(ATTRIBUTE_TABLE);

    for (i = 0; i < 8 * 8; i++) {
        vram_put(attributes[i]);
    }
}

void game_loop(void)
{
    while (1) {
        ppu_wait_nmi();
        pad = pad_poll(0);
        if (pad & PAD_LEFT) {
            scroll_x--;
        }
        if (pad & PAD_RIGHT) {
            scroll_x++;
        }
        if (pad & PAD_UP) {
            scroll_y--;
        }
        if (pad & PAD_DOWN) {
            scroll_y++;
        }
        scroll(scroll_x, scroll_y);
    }
}

void main(void)
{
    ppu_off();
    pal_bg(palette);
    bank_bg(1);
    fill_in_ppu_ram();
    fill_in_attributes();
    ppu_on_all();

    game_loop();
}
